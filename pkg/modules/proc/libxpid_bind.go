import "C"

func proc_dir_ls(pid int64) bool {
	x := C.proc_dir_ls(C.int(pid))
	if int(x) == 1 {
		return true
	}
	return false
}

func proc_dir_nav(pid int64) bool {
	x := C.proc_dir_nav(C.int(pid))
	if int(x) == 1 {
		return true
	}
	return false
}
