package filter

import api "github.com/kris-nova/xpid/pkg/api/v1"

var _ ProcessFilter = RetainOnlyNamed

// RetainOnlyNamed will only keep "named" PIDs
func RetainOnlyNamed(p *api.Process) bool {
	if p.Name != "" || p.Name == "\n" {
		return true
	}
	return false
}
